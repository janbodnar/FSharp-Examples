printfn "The bool is %b" true
printfn "The char is %c" 'F'
printfn "The string is %s" "falcon or \"falcon\" "
printfn "The int is %i" -3                        // signed
printfn "The int is %d" 42                        // same with %i
printfn "The float is %f" 42.0                    // signed
